// 根据slug获取单个文章
export function getBlogPostBySlug(slug: string): BlogPost | null {
  return mockBlogPosts.find(post => post.slug === slug && post.published) || null
}

// 获取所有已发布的文章
export function getAllBlogPosts(): BlogPost[] {
  return mockBlogPosts.filter(post => post.published)
}

// 获取相关文章
export function getRelatedPosts(currentPostId: string, keywords: string[], limit: number = 3): BlogPost[] {
  return mockBlogPosts
    .filter(post => post.published && post.id !== currentPostId)
    .filter(post => post.keywords.some(keyword => keywords.includes(keyword)))
    .slice(0, limit)
}

// 获取特色文章
export function getFeaturedPosts(limit: number = 3): BlogPost[] {
  return mockBlogPosts
    .filter(post => post.published && post.featured)
    .slice(0, limit)
}

// 按分类获取文章
export function getBlogPostsByCategory(category: string): BlogPost[] {
  if (category === 'all') {
    return getAllBlogPosts()
  }
  return mockBlogPosts.filter(post => post.published && post.category === category)
}

// 搜索文章
export function searchBlogPosts(query: string): BlogPost[] {
  const searchTerm = query.toLowerCase()
  return mockBlogPosts.filter(post =>
    post.published && (
      post.title.toLowerCase().includes(searchTerm) ||
      post.excerpt.toLowerCase().includes(searchTerm) ||
      post.keywords.some(keyword => keyword.toLowerCase().includes(searchTerm))
    )
  )
}
